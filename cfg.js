/**
 * Парсер текстовых оттенков админа. Переводит русское слово в структурированные HEX-коды.
 * Если совпадений нет, возвращает дефолтную строгую темно-серо-желтую (золотую) гамму.
 */
function parseAdminColorKeyword(keyword) {
    const cleanKey = (keyword || "").trim().toLowerCase();
    return AdminWordPresets[cleanKey] || AdminWordPresets["золотой"];
}

/**
 * Глобальная утилита вычисления вспомогательных оттенков (градиентов) на лету
 * на случай, если пользователь вручную ввел кастомный HEX/RGB цвет.
 */
function generateGradientsFromHex(hexColor) {
    // Вспомогательная функция конвертации HEX в RGB для манипуляций со светимостью
    let hex = hexColor.replace(/^#/, '');
    if (hex.length === 3) hex = hex.replace(/(.)/g, '$1$1');
    const r = parseInt(hex.substring(0, 2), 16);
    const g = parseInt(hex.substring(2, 4), 16);
    const b = parseInt(hex.substring(4, 6), 16);

    // Функция деликатного осветления/затемнения базового тона для сборки красивого UI-градиента
    const shiftColor = (factor) => {
        const clamp = (val) => Math.min(255, Math.max(0, Math.round(val)));
        const newR = clamp(r * factor);
        const newG = clamp(g * factor);
        const newB = clamp(b * factor);
        return `#${((1 << 24) + (newR << 16) + (newG << 8) + newB).toString(16).slice(1)}`;
    };

    return {
        primary: hexColor,
        start: shiftColor(1.2), // Слегка светлее для верхней точки градиента
        end: shiftColor(0.7),   // Плавное благородное затемнение книзу
        shadow: `rgba(${r}, ${g}, ${b}, 0.4)`
    };
}

